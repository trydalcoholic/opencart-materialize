		}

		$data['header'] = $this->load->controller('common/header');
		$data['column_left'] = $this->load->controller('common/column_left');
		$data['footer'] = $this->load->controller('common/footer');

		$this->response->setOutput($this->load->view('blog/author_form', $data));
	}

	protected function validateForm() {
		if (!$this->user->hasPermission('modify', 'blog/author')) {
			$this->error['warning'] = $this->language->get('error_permission');
		}

		foreach ($this->request->post['blog_author_description'] as $language_id => $value) {
			if ((utf8_strlen($value['name']) < 2) || (utf8_strlen($value['name']) > 64)) {
				$this->error['name'][$language_id] = $this->language->get('error_name');
			}
		}

		if (utf8_strlen($this->request->post['keyword']) > 0) {
			$this->load->model('blog/url_alias');

			$url_alias_info = $this->model_blog_url_alias->getUrlAlias($this->request->post['keyword']);

			if ($url_alias_info && isset($this->request->get['author_id']) && $url_alias_info['query'] != 'author_id=' . $this->request->get['author_id']) {
				$this->error['keyword'] = sprintf($this->language->get('error_keyword'));
			}

			if ($url_alias_info && !isset($this->request->get['author_id'])) {
				$this->error['keyword'] = sprintf($this->language->get('error_keyword'));
			}

			if ($this->error && !isset($this->error['warning'])) {
				$this->error['warning'] = $this->language->get('error_warning');
			}
		}

		return !$this->error;
	}

	protected function validateDelete() {
		if (!$this->user->hasPermission('modify', 'blog/author')) {
			$this->error['warning'] = $this->language->get('error_permission');
		}

		$this->load->model('blog/post');

		foreach ($this->request->post['selected'] as $author_id) {
			$post_total = $this->model_blog_post->getTotalPostsByAuthorId($author_id);

			if ($post_total) {
				$this->error['warning'] = sprintf($this->language->get('error_product'), $post_total);
			}
		}

		return !$this->error;
	}

	public function autocomplete() {
		$json = array();

		if (isset($this->request->get['filter_name'])) {
			$this->load->model('blog/author');

			$filter_data = array(
				'filter_name'	=> $this->request->get['filter_name'],
				'start'			=> 0,
				'limit'			=> 5
			);

			$results = $this->model_blog_author->getAuthors($filter_data);

			foreach ($results as $result) {
				$json[] = array(
					'author_id'	=> $result['author_id'],
					'name'		=> strip_tags(html_entity_decode($result['name'], ENT_QUOTES, 'UTF-8'))
				);
			}
		}

		$sort_order = array();

		foreach ($json as $key => $value) {
			$sort_order[$key] = $value['name'];
		}

		array_multisort($sort_order, SORT_ASC, $json);

		$this->response->addHeader('Content-Type: application/json');
		$this->response->setOutput(json_encode($json));
	}
